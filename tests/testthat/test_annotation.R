  alt <- c("-", "-", "-")
  dat <- data.table(chr = chr, start = start, end = end, ref = ref, alt = alt)
  x <- annotation.cols.match(dat, "avsnp147", database.dir = database.dir, return.col.names = "avSNP147")
  x <- as.data.frame(x)
  expect_that(colnames(x), equals("avSNP147"))
